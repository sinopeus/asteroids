package world.physics.vector;

/**
 * An enumeration of quadrants representing the four quadrants in any vector.
 * 
 * @author Tom Sydney Kerckhove & Xavier Goas Aguililla
 * @version 2.0
 */
public enum Quadrant
{
	/**
	 * The first quadrant.
	 */
	QUADRANT_I, 
	/**
	 * The second quadrant. 
	 */
	QUADRANT_II, 
	/**
	 * The third quadrant.
	 */
	QUADRANT_III, 
	/**
	 * The fourth quadrant. 
	 */
	QUADRANT_IV;
}